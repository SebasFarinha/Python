#Calculo de velocidad 
vo = int(input ("ingrese la velocidad inicial "))                                                            #vo es velociad inicial
g = 9.8                                                                                                     #g es gravedad
tiempo = int(input ("ingrese el tiempo "))
vf = vo + g * tiempo                                                                                        #vf es velocidad final
print("La velocidad final es de ",vf, "segundos")